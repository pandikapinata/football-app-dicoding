package com.example.pandu.finalproject.api

import android.net.Uri
import com.example.pandu.finalproject.BuildConfig

object APIService {

    fun getLeague(): String {
        return Uri.parse(BuildConfig.BASE_URL).buildUpon()
            .appendPath("api")
            .appendPath("v1")
            .appendPath("json")
            .appendPath(BuildConfig.TSDB_API_KEY)
            .appendPath("all_leagues.php")
            .build()
            .toString()
    }

